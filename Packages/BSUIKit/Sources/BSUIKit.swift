///
/// BSUIKit includes:
/// - `BaseViewController`: Generic view controller with ViewModel binding
/// - `BaseViewModel`: Observable-based view model with Input/State pattern
/// - `BaseView`: Generic UIView with setup and configuration
/// - `BaseRouter`: Navigation router with push/present/dismiss support
/// - `BaseSupporter`: Localization and analytics support
/// - `Observable`: Simple data binding for UIKit
/// - `BaseTableView` / `BaseCollectionView`: Generic list views

